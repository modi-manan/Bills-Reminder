package org.manan.model;

public class Passwords {
    private String hdfc;
    private String icici;
    private String axis;

    public String getHdfc() {
        return hdfc;
    }

    public void setHdfc(String hdfc) {
        this.hdfc = hdfc;
    }

    public String getIcici() {
        return icici;
    }

    public void setIcici(String icici) {
        this.icici = icici;
    }

    public String getAxis() {
        return axis;
    }

    public void setAxis(String axis) {
        this.axis = axis;
    }

}
